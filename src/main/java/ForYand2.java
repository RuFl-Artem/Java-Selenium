public class ForYand2 {
    public static void main(String[] args) {
        int days = 5, vitamin = 2;
        for (int i = 1; i <= days; i++) {
            System.out.println("День " + i);
            for (int j = 1; j <= vitamin; j++) {
                System.out.println("Витамин " + j);
            }
        }
    }
}
